! は、Swiftで "forced unwrapping" として知られる演算子です。
これは、オプショナル型の値をアンラップして、その中の値にアクセスするために使用されます。
これにより、オプショナル型の値から通常の値にアクセスできますが、
もし nil が含まれていた場合、実行時エラーが発生します。
##############################################
import Foundation
let n = Int(readLine()!)!
let m = Int(sqrt(Double(n)))
print(m * m)
##############################################
let n = Int(readLine()!)!
var x = 1
while (x + 1) * (x + 1) <= n {
    x += 1
}
print(x * x)
